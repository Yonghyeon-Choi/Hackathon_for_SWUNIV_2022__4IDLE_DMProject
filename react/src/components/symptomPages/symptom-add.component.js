import React, { useState } from "react";
import symptomService from "../../services/symptom.service";
import imageService from "../../services/image.service";
import "../GlobalStyles.css";

const SymptomAdd = () => {
  const initialSymptomState = {
    id: null,
    symptomid: "",
    symptomname: "",
    img: ""
  };
  const [symptom, setSymptom] = useState(initialSymptomState);
  const [submitted, setSubmitted] = useState(false);

  const handleInputChange = event => {
    event.preventDefault();
    const { name, value } = event.target;
    setSymptom({ ...symptom, [name]: value });
  };

  const [image, setImage] = useState("");
  const [preview, setPreview] = useState("");

  const onChange = (e) => {
    e.preventDefault();
    const img = e.target.files[0];
    console.log(img);
    setImage(img);
    setPreview(URL.createObjectURL(img));
  };

  const saveSymptom = () => {
    let data = {
      id: symptom.id,
      symptomid: symptom.symptomid,
      symptomname: symptom.symptomname,
      img: image.name
    };

    symptomService.create(data)
        .then(response => {
          setSymptom({
            id: response.data.id,
            symptomid: response.data.symptomid,
            symptomname: response.data.symptomname,
            img: response.data.img
          });
          setSubmitted(true);
          // console.log(response.data);
        })
        .catch(e => {
          console.log(e);
        });

    imageService.upload(image)
        .then(response => {
          setImage(response.image);
        })
        .catch(e => {
          console.log(e);
        });
  };

  const newSymptom = () => {
    setSymptom(initialSymptomState);
    setSubmitted(false);
  };

  return (
      <div className="submit-form">
        {submitted ? (
            <div>
              <h4>You submitted successfully!</h4>
              <button className="btn btn-success" onClick={newSymptom}>
                Add
              </button>
            </div>
        ) : (
            <div className={"edit-form"}>
              <h5>등록</h5>
              <hr/>
              <form>
                <div className="form-group">
                  <label htmlFor="symptomid">증상코드</label>
                  <input
                      type="text"
                      className="form-control"
                      id="symptomid"
                      name="symptomid"
                      placeholder="S0000"
                      value={symptom.symptomid}
                      onChange={handleInputChange}
                  />
                </div>
                <div className="form-group">
                  <label htmlFor="symptomname">증상이름</label>
                  <input
                      type="text"
                      className="form-control"
                      id="symptomname"
                      name="symptomname"
                      value={symptom.symptomname}
                      onChange={handleInputChange}
                  />
                </div>
              </form>
              <hr/>
              <button onClick={saveSymptom} className="addBtnStyle">
                등록
              </button>
            </div>
        )}
      </div>
  );
};

export default SymptomAdd;